# =========================
# 🧠 COACH AI (ПЕРСОНАЛЬНЫЙ ТРЕНЕР)
# =========================
def coach_message(plan, user=None):
    """
    Генерирует персональное сообщение тренера
    зависит от типа плана + состояния пользователя
    """

    # -------------------------
    # 🔥 CUT (ПОХУДЕНИЕ)
    # -------------------------
    if plan["type"] == "cut":
        base = "🔥 Сегодня работа на сжигание жира."

        # если есть streak — усиливаем мотивацию
        if user and user.get("streak", 0) >= 3:
            return base + " Сильная серия — не сбавляй темп!"
        return base + " Не пропускай шаги и держи дефицит калорий!"

    # -------------------------
    # 💪 BULK (НАБОР МАССЫ)
    # -------------------------
    if plan["type"] == "bulk":
        base = "💪 Масса строится через дисциплину."

        if user and user.get("energy", 100) < 40:
            return base + " Сегодня лучше лёгкая тренировка + белок."

        return base + " Ешь белок и не пропускай тренировки!"

    # -------------------------
    # 🧠 DISCIPLINE (ПРИВЫЧКИ)
    # -------------------------
    if plan["type"] == "discipline":
        return "🧠 Контроль важнее мотивации. Один день пропуска — не приговор."

    # -------------------------
    # 📱 CONTENT (КОНТЕНТ)
    # -------------------------
    if plan["type"] == "content":
        return "📱 Сегодня: идея → съёмка → публикация. Не усложняй."

    # -------------------------
    # 🧩 FALLBACK
    # -------------------------
    return "🧠 Держи фокус. Один день = один шаг вперёд."
